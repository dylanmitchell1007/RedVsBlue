using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace RedVsBlue
{
    class Party
    {
        List<Player> Squad = new List<Player>();
        /// <summary>
        /// This Party Default Constructor.
        /// </summary>
        public Party() { }
        /// <summary>
        /// This is the container with the Players in it.
        /// </summary>
        /// <param name="one"></param>
        /// <param name="two"></param>
        /// <param name="three"></param>
        public AddPlayer(p)
        {
            Squad.Add(p)
            p.EndTurn;
        }
        public Party(Player one, Player two, Player three)
        {
                Squad.Add(one);
                Squad.Add(two);
                Squad.Add(three);
        }
        
       
         
        
    
    }




}
